#include <stdio.h>
#include <stdlib.h>

// -------------------------------------------------------
// function declarations
// -------------------------------------------------------

// -------------------------------------------------------
// main
// -------------------------------------------------------

int main()
{
    // for loop
    for(int ii=0; ii<5; ii++)
    {
        printf("Hello Students\n");
    }

    // while loop
    int iter = 0, limit = 5;
    while(iter < limit)
    {
        iter += 1;
        printf("%d ", iter);
    }
    
    // do-while loop
    printf("\n");
    do
    {
        printf("%d ", iter);
        iter -= 1;
    } while (iter > 0);
    

    //
    return 0;
}

